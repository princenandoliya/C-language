#include <stdio.h>

int main()
{
	int a = 20;
	
	int b = 70;
	
	
	//addition
	
	printf("addition :%d\n",a+b);
	
	
	// substraction
	printf("substraction :%d\n",a-b);
	
	
	
	//multiplication
	
	printf("multiplication :%d\n",a*b);
	
	//division
	
	printf("division :%d\n",a / b);
	
	
	//modulo
	
	printf("modulo :%d",a%b);
}